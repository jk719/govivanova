// Helper function to sort categories by item count
export function getSortedCategories(): Category[] {
  return [...categories].sort((a, b) => {
    const aCount = parseInt(a.itemCount?.split(' ')[0] || '0')
    const bCount = parseInt(b.itemCount?.split(' ')[0] || '0')
    return bCount - aCount
  })
} 